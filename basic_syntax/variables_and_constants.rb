# INTRODUÇÃO: 
# Em Ruby, como em qualquer linguagem de programação, variáveis são como recipientes que armazenam dados,
# enquanto constantes são valores que não se alteram durante a execução do programa.
# Os tipos de dados definem a natureza desses dados, como números, textos ou valores lógicos.

# VARIÁVEIS:

# DECLARAÇÃO:
# Para declarar  uma variável em ruby, basta atribuir uma valor a ela. 
# Não há necessidade de declarar o tipo explicitamente, pois ruby é uma linguagem de tipagem dinâmica.

# NOMECLATURA: 
# Os nomes das variáveis devem começar com uma letra e podem conter letras e números.
# Todas as letras devem ser minúsculas e os espaços devem ser substituidos pelo caractere underscore (_), notação conhecida como snake_case.

# CASE-SENSITIVE: 
# Ruby diferencia letras maiúculas de minúsculas.

#EXEMPLOS:

name = 'João'
age = 30
height = 1.75
final_note = 9.5

# CONSTANTES:

# DELCARAÇÃO: 
# As constantes são declaradas com nomes em letras maiúsculas.
# É uma boa prática usar underscores para separar palavras em nomes de constantes.

# IMUTÁVEIS: 
# O valor de uma constante não deve ser alterado após sua definição.

# EXEMPLOS:

PI = 3.14159
PROGRAM_NAME = "Meu Primeiro Programa"

# *Obs: Embora seja recomendado não alterar constantes, Ruby permite essa ação. 
# No entanto, essa prática não é considerada boa e pode levar a erros inesperados.
